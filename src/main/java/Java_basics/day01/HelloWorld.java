package Java_basics.day01;

public class HelloWorld {
    //声明这个是一个公共的类，类的名字叫HelloWorld01
    //public公共，公开，代表这个类的访问权限是公开的，其它类可以访问它
    //class 类
    //HelloWorld01  类的名字
    //该方法的名字main(主要的)，简称main方法
    //main方法的作用：程序需要运行，必须先找到main方法，然后才能运行main方法里面的代码,相当于代码运行的一个入口

    public static void main(String[] args){//声明这个方法是一个公共的，静态的，无返回值的方法，名字叫main
        //公共的       静态的   无返回值的
        System.out.println("Hello World");//系统打印输出括号里面的内容
        //System  系统
        //out   输出
        //print   打印
        // ln   换行符
        System.out.println(1+3);
        System.out.println("************");
        //java里面所有的字符必须 加  "" 英文输入双引号


    }
}
